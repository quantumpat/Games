using GameEngine.Core;
using GameEngine.GameObjects;
using GameEngine.States;
using Microsoft.Xna.Framework;
using Microsoft.Xna.Framework.Graphics;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Epoch_One.States
{
	internal class StartMenuState : GameState
	{
		public StartMenuState(Engine engine, string name) : base(engine, name){}

		/*
		 * Game State Methods
		 */
		public override void Initialize()
		{

			base.Initialize();

		}

		public override void Load()
		{

			base.Load();

		}

		public override void Create(GameObjectManager gameObjects)
		{

			base.Create(gameObjects);

		}

		public override void Update(GameTime gameTime)
		{

			base.Update(gameTime);

		}

		public override void Draw(GameTime gameTime, SpriteBatch batch)
		{

			base.Draw(gameTime, batch);

		}

	}
}
